<h2 id="combat-decision-fatigue">combat decision fatigue</h2>
<p>A lot of times when I (or my friends) have trouble making a choice, we flip a coin. More accurately, we write some python.</p>
<pre><code>from random import choice
choice([&quot;bbq&quot;, &quot;sushi&quot;, &quot;italian&quot;])</code></pre>
<p>There's been some research saying that people can't make good decisions indefinitely, and flipping coins helps to pull some of the cognitive load off.</p>
<p>Tireless is a combination of coin-flipping and the <a href="http://www.pomodorotechnique.com/">pomodoro technique.</a> You put in some things you need to get done, and it tells you what to do and how long to do it for.</p>
<p>I got Tireless entirely into one html file, including the favicon.</p>
