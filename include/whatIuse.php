<h3 id="non-computer">Non-Computer</h3>
<p>It took me almost a full year to find a source of unruled notebooks. I finally lucked out on eBay, and bought a closet full of them. I go through about 2 per class and 1 per research topic, so I should have just enough of them for a PhD.</p>
<p>I also treated myself to a Pentel P207 mechanical pencil from an art store. It's not the fanciest one but, on most days, it costs more than the shirt I'm wearing. Such is grad school.</p>
<p>One afternoon, my little sister and I made whiteboards out of Home Depot shower supplies. It ended up costing about $25 for $300 worth of whiteboards. Go down to the hardware store and look for &quot;tile board&quot; that dry-erase marker comes off of easily. Bring a cleaner with you, so you aren't a vandal.</p>
<h2 id="software">Software</h2>
<h3 id="documents">Documents</h3>
<p>I keep my notes and code in at 10gb <a href="http://db.tt/hbFUfNV">Dropbox</a> account. Dropbox is great because you can run it in the background on the big university machine, and pleasant little notifications show up whenever your experiments dump a file to disk.</p>
<p>For papers, I have a separate <a href="http://spideroak.com">SpiderOak</a> account. You get 5gb for free, and it's nice to be able to have 5gb of papers without taking away 5gb of space for code. I have <a href="http://www.mendeley.com/">Mendeley</a> and <a href="http://www.zotero.org/">Zotero</a> - two research organizers - both pointed at that synchronized SpiderOak folder.</p>
<p>For note snippets, I use <a href="http://www.tiddlywiki.com/">TiddlyWiki</a>. It's basically a quick reference manual for me now. When I want to remember the formula for something or the name of a particular tool, I use this. I've tried using Evernote to fill this role, but I just end up using it as a bookmark system that I never go back through.</p>
<p>I discovered halfway through my first year that TiddlyWiki isn't so good for taking notes on whole papers. For those, I take notes by hand, and then copy the important parts down using a plaintext file and markdown. If there was a <a href="http://www.frescobaldi.org/index.html">frescobaldi</a> equivalent for LaTeX, I would use that instead. Markdown works well enough for me now, so I'm sticking with that.</p>
<p>For everything else, I use Google docs.</p>
<h3 id="data-mining-machine-learning">Data Mining &amp; Machine Learning</h3>
<p>I do a lot of my model prototyping in Python with Numpy and IPython. If I need a speedup, I'll generally move to Java with Eclipse. I know how to use <a href="http://www.cs.waikato.ac.nz/ml/weka/">Weka</a>, but I'm finding that as my research becomes more specific, I have less use for it. There was one project I needed C's speed for, but it wasn't comfortable.</p>
<p>Before I had a Matlab license, I used a lot of <a href="http://www.r-project.org/">R</a> with the incredible IDE <a href="http://rstudio.org/">RStudio</a>. I prefer Bayesian networks for their interpretability and causal implications, so I spend a lot of time with <a href="http://www.bnlearn.com/">bnlearn</a> and the <a href="http://code.google.com/p/bnt/">Bayes Net Toolbox</a>.</p>
<p>I never found the time to get fluent in vim or emacs, so I use <a href="http://www.jedit.org/">jedit</a>, <a href="http://www.sublimetext.com/2">sublime text 2</a>, and <a href="http://www.eclipse.org/">eclipse</a>.</p>
<p>Never underestimate Excel.</p>
<h3 id="general">General</h3>
